def add(n1, n2):
    return n1 + n2

def subtract(n1,n2):
    return n1 - n2

def multiply(n1, n2):
    return n1 * n2

def divide(n1, n2):
    return n1 / n2

operations = {"+": add, "-": subtract, "*": multiply, "/": divide}

cont = False
while not cont:
    first_digit = int(input("What is your first number?: "))

    cont = True
    while cont:
        chosen_operation = input('''
        +
        -
        *
        /
        
        Pick an operation from the above list: ''')

        second_digit = int(input("What is your second number?: "))

        result = operations[chosen_operation](n1 = first_digit,n2 = second_digit)
        print(f"{first_digit} {chosen_operation} {second_digit} = {result}")

        continue_program = input("Do you want to use this new number to continue calculating? Type yes or no: ")
        if continue_program == "no":
            cont = False
        else:
            first_digit = result